const BY_LANG = {
  en: { practice: EN_PRACTICE, games: EN_GAMES },
  ms: { practice: MS_PRACTICE, games: MS_GAMES },
} as const;

export function getOyenAskPracticeHint(lang: Language, topicId: TopicId, askIndex: number): string {
  const pool = BY_LANG[lang].practice[topicId];
  if (!pool?.length) {
    return BY_LANG[lang].practice.counting[askIndex % 10];
  }
  return pool[askIndex % pool.length];
}

export function getOyenAskGameHint(lang: Language, game: OyenGameId, askIndex: number): string {
  const pool = BY_LANG[lang].games[game];
  return pool[askIndex % pool.length];
}

export function oyenAskPoolSize(
  kind: 'practice' | 'game',
  id: TopicId | OyenGameId,
  lang: Language,
): number {
  if (kind === 'practice') {
    return BY_LANG[lang].practice[id as TopicId]?.length ?? 10;
  }
  return BY_LANG[lang].games[id as OyenGameId]?.length ?? 10;
}

