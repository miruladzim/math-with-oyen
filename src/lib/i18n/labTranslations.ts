export type LabTranslations = typeof labEn;

export function getLabModeTitle(lang: Language, mode: LabModeId): string {
  const pack = lang === 'ms' ? labMs : labEn;
  return pack.modes[mode].title;
}

export function getLabModeDesc(lang: Language, mode: LabModeId): string {
  const pack = lang === 'ms' ? labMs : labEn;
  return pack.modes[mode].desc;
}
